class PlayersHome(DataMixin, ListView):
    model = Player
    template_name = 'players/index.html'
    context_object_name = 'players'

    def get_context_data(self, *, object_list=None, **kwargs):
        context = super().get_context_data(**kwargs)
        c_def = self.get_user_context(title="Главная страница")
        return dict(list(context.items()) + list(c_def.items()))


class PlayerTeam(DataMixin, ListView):
    model = Player
    template_name = 'players/index.html'
    context_object_name = 'players'
    allow_empty = False

    def get_context_data(self, *, object_list=None, **kwargs):
        context = super().get_context_data(**kwargs)
        c_def = self.get_user_context(title='Команда - ' + str(context['players'][0].team),
                                      cat_selected=context['players'][0].team_id)

        return dict(list(context.items()) + list(c_def.items()))

    def get_queryset(self):
        return Player.objects.filter(team__slug=self.kwargs['team_slug'])


